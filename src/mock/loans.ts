/**
 * Generate a realistic repayment schedule for a given loan application.
 * Each installment includes principal + interest split.
 * Interest rate is monthly flat rate.
 */
export function generateRepaymentSchedule(app: LoanApplication): Repayment[] {
  const termMonths = app.termMonths ?? 12;
  const rate = (app.interestRate ?? 3.5) / 100;
  const principal = app.amount;
  const monthlyPrincipal = Math.round(principal / termMonths);
  const startDate = app.disbursedAt
    ? new Date(app.disbursedAt)
    : new Date(app.date.replace(/(\w+ \d+), (\d+)/, '$1 $2'));

  const repayments: Repayment[] = [];

  for (let i = 1; i <= termMonths; i++) {
    const dueDate = new Date(startDate);
    dueDate.setMonth(dueDate.getMonth() + i);

    const remaining = principal - monthlyPrincipal * (i - 1);
    const interest = Math.round(remaining * rate);
    const installmentAmount = monthlyPrincipal + interest;

    const isPast = dueDate < new Date();
    let status: Repayment['status'];
    if (app.status === 'Completed') {
      status = 'Paid';
    } else if (isPast && i <= Math.floor(termMonths * 0.6)) {
      status = 'Paid';
    } else if (i === Math.ceil(termMonths * 0.6) + 1 && isPast) {
      status = 'Upcoming';
    } else {
      status = 'Scheduled';
    }

    repayments.push({
      id: `REP-${app.id}-${i.toString().padStart(2, '0')}`,
      applicationId: app.id,
      userId: app.userId,
      installmentNumber: i,
      dueDate: dueDate.toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' }),
      amount: installmentAmount,
      principal: monthlyPrincipal,
      interest,
      penalty: 0,
      status,
      paidAt: status === 'Paid' ? dueDate.toISOString() : undefined,
    });
  }

  return repayments;
}

